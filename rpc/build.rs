use std::path::PathBuf;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let autogenerated_rust_output_directory = PathBuf::from("src/protos");
    let reflection = autogenerated_rust_output_directory.join("forge.bin");

    tonic_build::configure()
        .file_descriptor_set_path(&reflection)
        .include_file("common.rs")
        .type_attribute("forge.MachineState", "#[derive(serde::Serialize)]")
        .type_attribute("forge.MachineInterface", "#[derive(serde::Serialize)]")
        .type_attribute("forge.UUID", "#[derive(serde::Serialize)]")
        .type_attribute(".", "#[allow(unknown_lints, clippy::all)]") // don't lint generated code
        .build_server(true)
        .build_client(true)
        .protoc_arg("--experimental_allow_proto3_optional")
        .out_dir(autogenerated_rust_output_directory)
        .compile(
            &["proto/forge.proto", "proto/machine_discovery.proto"],
            &["proto"],
        )
        .unwrap();

    Ok(())
}
