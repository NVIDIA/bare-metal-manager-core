use std::net::Ipv4Addr;

use ::rpc::forge as rpc;
use gtmpl_derive::Gtmpl;
use utils::models::dhcp::HostConfig;

/// The path we write in non-NVUE mode, when using dhcp-relay (versus dhcp-server)
pub const RELAY_PATH: &str = "etc/supervisor/conf.d/default-isc-dhcp-relay.conf";

/// The path NVUE writes if we're using dhcp-relay
pub const RELAY_PATH_NVUE: &str = "etc/supervisor/conf.d/isc-dhcp-relay-default";

pub const SERVER_PATH: &str = "etc/supervisor/conf.d/default-forge-dhcp-server.conf";
pub const SERVER_CONFIG_PATH: &str = "var/support/forge-dhcp/conf/dhcp.yaml";
pub const SERVER_HOST_CONFIG_PATH: &str = "var/support/forge-dhcp/conf/host.yaml";

pub const TMPL_EMPTY: &str = "# Auto-generated by Forge!";
const TMPL_FULL_ETV: &str = include_str!("../templates/default-isc-dhcp-relay_etv.conf");

const TMPL_SERVER: &str = include_str!("../templates/default-forge-dhcp-server.conf");

pub const RELOAD_CMD: &str = "supervisorctl update";
// Since we don't update default-forge-dhcp-server.conf file, supervisord does not restart it on
// `supervisorctl update` command.
pub const RELOAD_DHCP_SERVER: &str =
    "supervisorctl update;supervisorctl restart forge-dhcp-server-default";

/// Generate default-isc-dhcp-relay.conf
pub fn build_relay_config(conf: DhcpRelayConfig) -> Result<String, eyre::Report> {
    let params = TmplDHCRelayConfigParameters {
        Uplinks: conf.uplinks,
        VlanIDs: conf.vlan_ids,
        Servers: conf.dhcp_servers.iter().map(|ip| ip.to_string()).collect(),
        RemoteId: conf.remote_id,
    };
    gtmpl::template(TMPL_FULL_ETV, params).map_err(|e| e.into())
}

/// Generate default-forge-dhcp-server.conf
pub fn build_server_supervisord_config(
    conf: DhcpServerSupervisordConfig,
) -> Result<String, eyre::Report> {
    let params = TmplDHCServerConfigParameters {
        VlanIDs: conf.vlan_ids,
    };
    gtmpl::template(TMPL_SERVER, params).map_err(|e| e.into())
}

/// An empty default-isc-dhcp-relay.conf
pub fn blank() -> String {
    gtmpl::template(TMPL_EMPTY, "").expect("dhcp blank template cannot fail")
}

pub fn build_server_config(
    pxe_ip: Ipv4Addr,
    ntp_ip: Option<Ipv4Addr>,
    nameservers: Vec<Ipv4Addr>,
    loopback_ip: Ipv4Addr,
) -> Result<String, eyre::Report> {
    let dhcp_config = utils::models::dhcp::DhcpConfig::from_forge_dhcp_config(
        pxe_ip,
        ntp_ip,
        nameservers,
        loopback_ip,
    )?;

    Ok(serde_yaml::to_string(&dhcp_config)?)
}

pub fn build_server_host_config(
    conf: rpc::ManagedHostNetworkConfigResponse,
) -> Result<String, eyre::Report> {
    Ok(serde_yaml::to_string(&HostConfig::try_from(conf)?)?)
}

pub struct DhcpRelayConfig {
    pub uplinks: Vec<String>,
    pub vlan_ids: Vec<u32>,
    pub dhcp_servers: Vec<Ipv4Addr>,
    pub remote_id: String,
}

pub struct DhcpServerSupervisordConfig {
    pub vlan_ids: Vec<String>,
}

//
// Go template object, hence allow(non_snake_case)
//

#[allow(non_snake_case)]
#[derive(Clone, Gtmpl)]
struct TmplDHCRelayConfigParameters {
    Uplinks: Vec<String>,
    VlanIDs: Vec<u32>,
    Servers: Vec<String>,
    RemoteId: String,
}

#[allow(non_snake_case)]
#[derive(Clone, Gtmpl)]
struct TmplDHCServerConfigParameters {
    VlanIDs: Vec<String>,
}

#[cfg(test)]
mod tests {
    use super::{build_relay_config, DhcpRelayConfig};

    #[test]
    fn test_write_dhcp() -> Result<(), Box<dyn std::error::Error>> {
        let params = DhcpRelayConfig {
            uplinks: vec!["p0_sf".to_string(), "p1_sf".to_string()],
            vlan_ids: vec![177],
            dhcp_servers: vec![[10, 180, 248, 25].into()],
            remote_id: "test".to_string(),
        };
        let output = build_relay_config(params)?;
        assert_eq!(
            output,
            include_str!("../templates/default-isc-dhcp-relay.conf.expected")
        );
        Ok(())
    }
}
