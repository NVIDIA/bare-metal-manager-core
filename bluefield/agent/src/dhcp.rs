use std::net::Ipv4Addr;

use gtmpl_derive::Gtmpl;

use ::rpc::forge as rpc;

pub const PATH: &str = "etc/supervisor/conf.d/default-isc-dhcp-relay.conf";
const TMPL_EMPTY: &str = "# Auto-generated by Forge!";
const TMPL_FULL_ETV: &str = include_str!("../templates/default-isc-dhcp-relay_etv.conf");
const TMPL_FULL_FNN: &str = include_str!("../templates/default-isc-dhcp-relay_fnn.conf");
pub const RELOAD_CMD: &str = "supervisorctl update";

/// Generate default-isc-dhcp-relay.conf
pub fn build(conf: DhcpConfig) -> Result<String, eyre::Report> {
    let params = TmplDHCRelayConfigParameters {
        Uplinks: conf.uplinks,
        VlanIDs: conf.vlan_ids,
        Servers: conf.dhcp_servers.iter().map(|ip| ip.to_string()).collect(),
        RemoteId: conf.remote_id,
    };
    let tmpl_path = match conf.network_virtualization_type {
        None => TMPL_FULL_ETV,
        Some(x) if x == rpc::VpcVirtualizationType::EthernetVirtualizer as i32 => TMPL_FULL_ETV,
        Some(x) if x == rpc::VpcVirtualizationType::ForgeNativeNetworking as i32 => TMPL_FULL_FNN,
        Some(x) => {
            eyre::bail!("Invalid network_virtualization_type {x}");
        }
    };
    gtmpl::template(tmpl_path, params).map_err(|e| e.into())
}

/// An empty default-isc-dhcp-relay.conf
pub fn blank() -> String {
    gtmpl::template(TMPL_EMPTY, "").expect("dhcp blank template cannot fail")
}

pub struct DhcpConfig {
    pub uplinks: Vec<String>,
    pub vlan_ids: Vec<u32>,
    pub dhcp_servers: Vec<Ipv4Addr>,
    pub remote_id: String,
    pub network_virtualization_type: Option<i32>,
}

//
// Go template object, hence allow(non_snake_case)
//

#[allow(non_snake_case)]
#[derive(Clone, Gtmpl)]
struct TmplDHCRelayConfigParameters {
    Uplinks: Vec<String>,
    VlanIDs: Vec<u32>,
    Servers: Vec<String>,
    RemoteId: String,
}

#[cfg(test)]
mod tests {
    use super::{build, DhcpConfig};

    #[test]
    fn test_write_dhcp() -> Result<(), Box<dyn std::error::Error>> {
        let params = DhcpConfig {
            uplinks: vec!["p0_sf".to_string(), "p1_sf".to_string()],
            vlan_ids: vec![177],
            dhcp_servers: vec![[10, 180, 248, 25].into()],
            remote_id: "test".to_string(),
            network_virtualization_type: None,
        };
        let output = build(params)?;
        assert_eq!(
            output,
            include_str!("../templates/default-isc-dhcp-relay.conf.expected")
        );
        Ok(())
    }
}
